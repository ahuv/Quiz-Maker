
public class Person
{
	private String name;
	private String password;

	public Person(String name, String password)
	{
		this.name = name;
		this.password = password;
	}

	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @return the password
	 */
	public String getPassword()
	{
		return password;
	}

}
